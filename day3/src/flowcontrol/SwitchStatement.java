package flowcontrol;

public class SwitchStatement {
	
	public static void main(String[] args) {
		char letter = 'A';
		
		switch(letter) {
		case 'A':
			System.out.println("A is for Aerosmith");
		case 'B':
			System.out.println("B is for beastie boys");
			break;
		case 'C':
			System.out.println("C is for Chamillionaire");
			break;
		}
	}

}
